
public class Person {
private String name;
private String Add;
public Person(String name, String add) {
	super();
	this.setName(name);
	setAdd(add);
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAdd() {
	return Add;
}
public void setAdd(String add) {
	Add = add;
}

}
